package com.qa.encapsulation;

public class Person {
	
	private String name;
	private int age;
	private int acc;
	
	
	// getters + setters
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public int getAcc() {
		return acc;
	}
	public void setAcc(int acc) {
		this.acc = acc;
	}
	
	
	
	
}
